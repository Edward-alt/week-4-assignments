class EdwardsClass {
  static myStaticProperty = "Hello, I am a static property!";
  static myStaticMethod() {
    return "Hello, I am a static method!";
  }
}

console.log(EdwardsClass.myStaticProperty); // Output: "Hello, I am a static property!"
console.log(EdwardsClass.myStaticMethod()); // Output: "Hello, I am a static method!"

